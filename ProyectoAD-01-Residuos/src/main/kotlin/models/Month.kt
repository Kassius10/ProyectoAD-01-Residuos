package models

/**
 * Enum de meses del año
 * @param month nombre del mes del año
 */
enum class Month(month: String) {
    ENERO("enero"),
    FEBRERO("febrero"),
    MARZO("marzo"),
    ABRIL("abril"),
    MAYO("mayo"),
    JUNIO("junio"),
    JULIO("julio"),
    AGOSTO("agosto"),
    SEPTIEMBRE("septiembre"),
    OCTUBRE("octubre"),
    NOVIEMBRE("noviembre"),
    DICIEMBRE("diciembre");

}